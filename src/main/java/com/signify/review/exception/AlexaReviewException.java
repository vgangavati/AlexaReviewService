package com.signify.review.exception;

public class AlexaReviewException extends Exception {

	private static final long serialVersionUID = 8496139037513660566L;

	public AlexaReviewException() {
		super();

	}

	public AlexaReviewException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

	public AlexaReviewException(String message, Throwable cause) {
		super(message, cause);

	}

	public AlexaReviewException(String message) {
		super(message);

	}

	public AlexaReviewException(Throwable cause) {
		super(cause);

	}

}
